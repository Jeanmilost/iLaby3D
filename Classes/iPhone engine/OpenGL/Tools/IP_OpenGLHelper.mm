#import "IP_OpenGLHelper.h"

//------------------------------------------------------------------------------
// class IP_OpenGLHelper - objective c
//------------------------------------------------------------------------------
@implementation IP_OpenGLHelper
//------------------------------------------------------------------------------
+ (E_Matrix16)OpenGLMatrixToEMatrix :(const IP_OpenGLMatrix&)openGLMatrix
{
    E_Matrix16 result;

    // copy world matrix
    for (unsigned i = 0; i < 4; ++i)
        for (unsigned j = 0; j < 4; ++j)
            result.m_Table[i][j] = openGLMatrix.m_Table[(i * 4) + j];
    
    return result;
}
